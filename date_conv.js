function formatDate(dateString) {
  const date = new Date(dateString);
  const options = {
    day: '2-digit',
    month: '2-digit',
    year: 'numeric',
    timeZone: 'Asia/Bangkok',
    //timeZoneName: 'short'
  };
  return date.toLocaleDateString('en-GB', options);
}

